    private void insertTable() {
        //adattábla kiürítése
        int sorokSzama = model.getRowCount();
        for (int i = sorokSzama; i > 0; i--) {
            model.removeRow(i);
        }
        
        //Adattábal feltöltése a listából
        for (FaktClass f : list) {
            model.addRow(new Object[]{f.getNev(),f.getTantargy(),f.getJegy()});
            System.out.println(f.getNev());
        }
    }

    private void insertComboBox() {
        //Tantárgyak kiürítése
        ArrayList<String> tantargyak = new ArrayList<>();
        for (FaktClass f : list) {
            if (!tantargyak.contains(f.getTantargy())) {
                tantargyak.add(f.getTantargy());
            }
        }
        //tantárgyak beírása comboboxba
        for (String t : tantargyak) {
            jComboBox1.addItem(t);
        }
        
    }
