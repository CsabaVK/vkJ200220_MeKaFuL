    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        String valTantargy = jComboBox1.getSelectedItem().toString();
        int osszeg = 0;
        int tantargyDB = 0;
        for (FaktClass f : list) {
            if (f.getTantargy().equals(valTantargy)) {
                osszeg += f.getJegy();
                tantargyDB++;
            }
        }
        double atlag = osszeg / tantargyDB;
        tf_jegy.setText(Double.toString(atlag));
        
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void bt_adatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_adatActionPerformed
        // TODO add your handling code here:
        //ADATOK BEOLVASÁSA
        try {
            FileReader fajl = new FileReader("fakt.txt");
            BufferedReader br = new BufferedReader (fajl);
            String sor = "";
            
            while ((sor = br.readLine()) !=null) {
                FaktClass f = new FaktClass(sor);
                list.add(f);
            }
            System.out.println("Beolvasás OK!");
            System.out.println("A lista elemszáma:" + list.size());
        }
        catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Hiba a beolvasáskor!");
            System.out.println("HIBA " + e);
        }
        
        insertTable();
        bt_adat.setEnabled(false);
        insertComboBox();
        
        
    }//GEN-LAST:event_bt_adatActionPerformed

